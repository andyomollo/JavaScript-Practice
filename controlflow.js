// Control flow statements

const age = 60;

if (age >= 60){
    console.log ("You're old");
}else{
    console.log ("You're Young");
}

//switch statement
const day = "Monday";
switch (day) {
    case "Monday":
        console.log("Start of the week.");
        break;
    case "Friday":
        console.log("End of the workweek.");
        break;
    default:
        console.log("It's a regular day.");
}
